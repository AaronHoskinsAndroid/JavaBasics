package examples.aaronhoskins.com.javabasics;

public class DataTypes {
    /**
     *  Primitives
     */
    //Whole numbers (not fractional)
    int integerValue = 0;     //ranging from -2147483648 to 2147483647
    short shortValue = 0;     //ranging from -32,768 to 32,767
    long longValue = 0;       //ranging from  -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
    //Fractional Numbers
    float floatValue = 0.0f;  //single-precision(32bit)
    double doubleValue = 0.0; //double-precision(64bit)
    //boolean values (True or False)
    boolean booleanValue = true;
    //Unicode character
    char charValue = 'a';

    /**
     *  Object Types
     */
    Integer integerObject = null;
    Short shortObject = null;
    Long longObject = null;
    Float floatObject = null;
    Double doubleObject = null;
    Boolean booleanObject = null;
    Character characterObject = null;
}
